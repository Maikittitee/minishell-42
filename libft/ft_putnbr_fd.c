#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	s;
	int		i;

	if (n == -2147483648)
		ft_putstr_fd("-2147483648", fd);
	else if (n == 0)
		write (fd, "0", 1);
	else
	{
		if (n < 0)
		{
			write (fd, "-", 1);
			n *= -1;
		}
		i = 1;
		while (n / i >= 10)
			i *= 10;
		while (i)
		{
			s = n / i + '0';
			write (fd, &s, 1);
			n %= i;
			i /= 10;
		}
	}
}
