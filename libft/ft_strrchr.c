char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (*s != '\0')
	{
		i++;
		s++;
	}
	while (i >= 0)
	{
		if (*s == (char)c)
			return ((char *)s);
		else
		{
			i--;
			s--;
		}
	}
	if ((char)c == '\0')
		return ((char *)s);
	return (0);
}
